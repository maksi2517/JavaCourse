package notSolved;

public class MainInt {
    public static void main(String[] args) {
        //Werte die direkt in Programmen stehen: (numerische) Literale
        //numerische Literale werden einem Typen zugewiesen
        System.out.println((24*60*60*1000*1000) / (24*60*60*1000));
        System.out.println((24*60*60*1000*1000l) / (24*60*60*1000));
        System.out.println((int) 24*60*60*1000*1000);
        System.out.println((int) 24*60*60*1000);
        System.out.println(24*60*60*1000*1000l);
        System.out.println(24*60*60*1000l);
    }
}
